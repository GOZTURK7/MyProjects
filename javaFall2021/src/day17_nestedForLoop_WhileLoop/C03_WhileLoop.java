package day17_nestedForLoop_WhileLoop;

public class C03_WhileLoop {

	public static void main(String[] args) {


		/*
		 20'den 30'a kadar olan sayıları 20 ve 30 dahil
		 while loop ile yazdırın.
		 
		 */
		
		int i=20;   // while de bu 1. kısım (Starting Value);
		
		while(i<31) {
			
			System.out.print(i + " ");
			i++; // ve bu 3. kısım (increament) elle yazıyorUZ.
		}
		
		/*
		 NOTE: for loop ile çözebilceğimiz sorularda 
		 while loop kullanmak kodumuzu biraz uzatır.
		 */
		
		
	}
	
	

}
