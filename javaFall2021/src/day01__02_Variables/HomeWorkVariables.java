package day01__02_Variables;

public class HomeWorkVariables {

	public static void main(String[] args) {

		// 1)  FARKLI 3 DATA TÜRÜNDE VARİABLE OLUŞTURUN VE BUNLARI YAZDIRIN.
		String adı = "Gökhan Öztürk";
		char karakter = '&';
		int sayi = 86;
		float ortalama = 2.5555f;
		
		System.out.println(adı);
		System.out.println(karakter);
		System.out.println(sayi);
		System.out.println(ortalama);
		System.out.println(karakter + sayi);
		
		/* 2)  İSİM VE SOYİSİM İÇİN İKİ VARİABLE OLUŞTURUN VE BUNLARI
		 		İSMİNİZ : GÖKHAN
		 		SOY İSMİNİZ : ÖZTÜRK ŞEKLİNDE YAZDIRIN */

		
		String isim = "Gökhan";
		String soyisim = "ÖZTÜRK";
		
		System.out.println("İsminiz: " + isim);
		System.out.println("Soy İsminiz: " + soyisim);
		
		
		
		/* 3) İKİ FARKLI TAMSAYI DATA TÜRÜNDE 2 VARİABLE OLUŞTURUN BUNLARIN TOPLAMINI YAZIN*/
		
		int sayi1 = 35;
		int sayi2 = 75;
		
		System.out.println(sayi1 + sayi2);
		
		
		// 4) BİR TAM SAYI VE BİR ONADLIKLI SAYI VARİABLE OLUŞTURUN VE BUNLARIN TOPLAMINI YAZDIRIN
		
		int sayı1 = 30;
		double sayı2 = 30.5;
		
		System.out.println(sayı1 + sayı2);
		
		// 5) char DATA TÜRÜNDE BİR VARİABLE OLUŞTURUN VE YAZDIRIN.
		char karakter2 = '?';
		
		System.out.println(karakter2);
		
		
		// 6) BİR TAM SAYI VE BİR char DEĞİŞKEN OLUŞTURUN VE BUNLARIN TOPLAMINI YAZDIRIN.
		int sayı3 = 50;
		char karakter3 = '=';
		
		System.out.println(sayı3 + karakter3);
		
		
		
		
		
		
		
		
	}

}
